    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed
        int valor;
        String indice = txtIndice.getText();
        valor = Integer.parseInt(indice);
        if (valor > 0) {
            valor -= 1;
            indice = String.valueOf(valor);
            txtIndice.setText(indice);
            txtDni.setText(dni[valor]);
            txtNombre.setText(nombre[valor]);
            txtApellido.setText(apellido[valor]);
            txtDireccion.setText(direccion[valor]);
            txtTelefono.setText(telefono[valor]);
            txtFechaNac.setText(fechaNac[valor]);
        }
    }//GEN-LAST:event_btnAtrasActionPerformed

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        String indice = txtIndice.getText();
        int valor = Integer.parseInt(indice);
        dni[valor] = txtDni.getText();
        nombre[valor] = txtNombre.getText();
        apellido[valor] = txtApellido.getText();
        direccion[valor] = txtDireccion.getText();
        telefono[valor] = txtTelefono.getText();
        fechaNac[valor] = txtFechaNac.getText();

    }//GEN-LAST:event_btnGrabarActionPerformed

