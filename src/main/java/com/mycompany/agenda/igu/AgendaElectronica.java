    private void btnAdelanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdelanteActionPerformed
        int valor;
        String indice = txtIndice.getText();
        valor = Integer.parseInt(indice);
        if (valor < 9) {
            valor += 1;
            indice = String.valueOf(valor);
            txtIndice.setText(indice);
            txtDni.setText(dni[valor]);
            txtNombre.setText(nombre[valor]);
            txtApellido.setText(apellido[valor]);
            txtDireccion.setText(direccion[valor]);
            txtTelefono.setText(telefono[valor]);
            txtFechaNac.setText(fechaNac[valor]);
        }

    }//GEN-LAST:event_btnAdelanteActionPerformed

